// Example of Local Var
public class LocalVar {
      // static Variabels
      static int a=67;
      static int b=9;
    public static void main(String[] args) {
           // Local Variable
          int result = 0;
          result = a+b;
        System.out.println(result);
        subtraction();
    }
    public static void subtraction()
    {
        int result = a-b;
        System.out.println(result);
    } 
}
