// Local Variabels
public class LocalVar {
      static int a=67;
      static int b=9;
    public static void main(String[] args) {
           
          int result = 0;
          result = a+b;
        System.out.println(result);
        subtraction();
    }
    public static void subtraction()
    {
        int result = a-b;
        System.out.println(result);
    } 
}
